# operator >
print ('Ini benar gak sih 7>4 ?',7>4)
print ('Ini benar gak sih  8>8 ?',8>8)


# operator <
print ('Ini benar gak sih 7<4 ?',7<4)
print ('Ini benar gak sih  8<8 ?',8<8)


# operator >=
print ('Ini benar gak sih 7>=4 ?',7>=4)
print ('Ini benar gak sih  8>=8 ?',8>=8)


# operator <=
print ('Ini benar gak sih 7<=4 ?',7<=4)
print ('Ini benar gak sih  8<=8 ?',8<=8)


# operator!=
print ('Ini benar gak sih 7!=4 ?',7!=4)
print ('Ini benar gak sih  8!=8 ?',8!=8)


