buku = 9
pensil = 5

# penjumlahan 
hasil = buku + pensil
print('Hasil penjumlahan :',hasil)

# pengurangan 
hasil = buku - pensil
print('Hasil pengurangan :',hasil)

# perkalian
hasil = buku * pensil
print('Hasil perkalian :',hasil)

# pangkat
hasil = buku ** pensil
print ('Hasil ppangkat :',hasil)

# pembagian 
hasil = buku / pensil
print('Hasil pembagian  :',hasil)

# pembagian kebawh
hasil = buku // pensil
print('Hasil pembagian kebawh :',hasil)

# sisa bagi
hasil = buku % pensil
print('Hasil sisa bagi :',hasil)

# precedence
hasil = 3 + 6 * 8 - 5
print('Hasil precedence :',hasil)

# precedence
